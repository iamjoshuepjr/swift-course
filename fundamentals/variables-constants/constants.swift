/* To declare constants you need use the let keyword. 
   Swift constants are much more powerful than C constants. */

let country = "United States of America"
let capital = "Washington, D.C."
let founding = 1776
let language = "English"
let current = "$"
let largestCity = "New York City"

print(
    """
    \n
    Country: \(country)
    Capital City: \(capital)
    Founding Year: \(founding)
    Language: \(language)
    Current: (\(current))
    Largest City: \(largestCity)
    \n
    """)